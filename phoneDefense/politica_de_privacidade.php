    <h1>Política de Privacidade</h1>

    <p><strong>Última atualização:</strong> 25 de Outubro de 2025</p>

    <h2>1. Coleta de Informações</h2>
    <p>O <strong>Phone Defense</strong> não coleta, armazena ou compartilha quaisquer informações pessoais dos usuários.
        Todas as funcionalidades operam localmente no dispositivo.</p>

    <h2>2. Funcionalidades que Requerem Permissões</h2>

    <h3>📷 Câmera</h3>
    <p><strong>Finalidade:</strong> Capturar fotos de segurança quando movimento não autorizado é detectado.<br>
        <strong>Uso:</strong> As fotos são armazenadas apenas localmente no dispositivo e não são compartilhadas.
    </p>

    <h3>🎤 Microfone</h3>
    <p><strong>Finalidade:</strong> Detectar assovios para localizar o dispositivo.<br>
        <strong>Uso:</strong> O áudio é processado localmente e não é gravado ou transmitido.
    </p>

    <h3>⚡ Sensores e Serviços em Primeiro Plano</h3>
    <p><strong>Finalidade:</strong> Monitorar estado da bateria, conexão do carregador, e detectar movimento.<br>
        <strong>Uso:</strong> Operações locais para alertas de segurança.
    </p>

    <h2>3. Publicidade (AdMob)</h2>
    <p>Utilizamos o Google AdMob para exibir anúncios. O AdMob pode coletar dados conforme sua própria política de
        privacidade. Recomendamos consultar a <a href="https://policies.google.com/privacy">Política de Privacidade do
            Google</a>.</p>

    <h2>4. Armazenamento Local</h2>
    <p>Todas as fotos, configurações e dados do app são armazenados exclusivamente no seu dispositivo e podem ser
        excluídos desinstalando o aplicativo.</p>

    <h2>5. Controle do Usuário</h2>
    <p>Você pode revogar qualquer permissão através das Configurações do Android. Algumas funcionalidades podem deixar
        de operar sem as permissões necessárias.</p>

    <h2>6. Contato</h2>
    <p>Dúvidas sobre esta política: <a href="https://www.talento.dev.br">www.talento.dev.br</a></p>
