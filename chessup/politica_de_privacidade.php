    <div>
        <strong>Resumo:</strong>
        Este aplicativo de xadrez não exige cadastro, não realiza login e não coleta dados pessoais dos usuários.
    </div><br>

    <h2>1. Informações Coletadas</h2>
    <p>
        Nosso aplicativo <strong>não coleta informações pessoais</strong> como nome, e-mail, telefone ou dados de
        identificação.
    </p>
    <p>
        Todas as partidas são registradas de forma <strong>anônima</strong>, sem qualquer vínculo com a identidade do
        usuário.
    </p>

    <h2>2. Dados de Jogo (Anônimos)</h2>
    <p>
        Para funcionamento do ranking e estatísticas, o aplicativo pode armazenar dados relacionados às partidas,
        como:
    </p>
    <ul>
        <li>Resultado da partida</li>
        <li>Movimentos do jogo</li>
        <li>Data e duração da partida</li>
    </ul>
    <p>
        Esses dados são armazenados de forma <strong>anônima</strong> utilizando a plataforma Supabase e não permitem a
        identificação do jogador.
    </p>

    <h2>3. Uso da Internet</h2>
    <p>
        O aplicativo requer acesso à internet para:
    </p>
    <ul>
        <li>Salvar partidas anônimas</li>
        <li>Carregar rankings globais</li>
        <li>Sincronizar dados de jogo</li>
    </ul>

    <h2>4. Compartilhamento de Dados</h2>
    <p>
        Nenhuma informação é compartilhada com terceiros para fins comerciais, publicitários ou de rastreamento.
    </p>

    <h2>5. Cookies</h2>
    <p>
        Este aplicativo <strong>não utiliza cookies</strong> nem tecnologias semelhantes de rastreamento.
    </p>

    <h2>6. Crianças</h2>
    <p>
        O aplicativo não é direcionado especificamente a crianças e não coleta intencionalmente dados pessoais de
        menores.
    </p>

    <h2>7. Segurança</h2>
    <p>
        Utilizamos boas práticas de segurança para proteger os dados anônimos armazenados, incluindo conexões seguras e
        infraestrutura confiável.
    </p>

    <h2>8. Custos</h2>
    <p>
        O aplicativo é disponibilizado gratuitamente, sem compras obrigatórias.
    </p>

    <h2>9. Alterações nesta Política</h2>
    <p>
        Caso esta Política de Privacidade seja alterada, a versão atualizada será publicada nesta página.
    </p>

